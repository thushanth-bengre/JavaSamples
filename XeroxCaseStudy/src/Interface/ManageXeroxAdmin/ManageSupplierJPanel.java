    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = supplierlistTable.getSelectedRow();
        
        if(selectedRowValue >=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to Delete this Supplier", "Warning" , dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
            Supplier s= (Supplier)supplierlistTable.getValueAt(selectedRowValue, 0);
            sd.removeSupplier(s);
            JOptionPane.showMessageDialog(null,"Record Deleted");
            populateTable();
        }
        }
            else
            JOptionPane.showMessageDialog(null,"Please select a record to delete");        
    }//GEN-LAST:event_deletebtnActionPerformed


