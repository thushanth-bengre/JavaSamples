    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = supplierlistTable.getSelectedRow();
        if(selectedRowValue >= 0){
        Supplier s= (Supplier)supplierlistTable.getValueAt(selectedRowValue, 0);
        UpdateSupplierJPanel panel = new UpdateSupplierJPanel(userProcessContainer,s);
        userProcessContainer.add("UpdateSupplierJPanel",panel);
        CardLayout layout =(CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
         else
            JOptionPane.showMessageDialog(null,"Please select a record to view");
    }//GEN-LAST:event_updatebtnActionPerformed

