    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)supplierlistTable.getModel();
        dtm.setRowCount(0);
        for(Supplier s:sd.getListOfSuppliers()){
            Object row[] = new Object[1];
            row[0] = s;
            dtm.addRow(row);
    }
    }

