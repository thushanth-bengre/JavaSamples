    private void viewbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewbtnActionPerformed
        // TODO add your handling code here:
        refreshTextBoxes();
        int selectedRowValue = personlistTable.getSelectedRow();
        if(selectedRowValue >= 0){
        Person p= (Person)personlistTable.getValueAt(selectedRowValue, 0);
        HashMap<String,User> temphash = ud.getUserMap();
        User user = null;
            for(String name: temphash.keySet()){
            User u= temphash.get(name);
            if(u.getP().equals(p)){
                user = u;
                }
            }
            if(user == null){
              JOptionPane.showMessageDialog(null,"User Credentails not created for this Person. Please create user by clicking on Add User button!!");   
              createbtn.setEnabled(true);
            } else{
                usernameTextField.setText(user.getUserName());
                passwordTextField.setText(user.getPassword());
            }
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record to view");            
    }//GEN-LAST:event_viewbtnActionPerformed


