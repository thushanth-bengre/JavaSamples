    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)personlistTable.getModel();
        dtm.setRowCount(0);
        for(Person p: pd.getListOfPerson()){
            Object row[] = new Object[3];
            row[0] = p;
            row[1] = p.getLastName();
            row[2] = p.getAge();
            dtm.addRow(row);
    }
    }
    
