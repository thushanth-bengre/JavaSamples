    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = marketlistTable.getSelectedRow();
        if(selectedRowValue >= 0){
        Market m= (Market)marketlistTable.getValueAt(selectedRowValue, 0);
        UpdateMarketJPanel panel = new UpdateMarketJPanel(userProcessContainer,m);
        userProcessContainer.add("UpdateMarketJPanel",panel);
        CardLayout layout =(CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
         else
            JOptionPane.showMessageDialog(null,"Please select a record to view");
    }//GEN-LAST:event_updatebtnActionPerformed

