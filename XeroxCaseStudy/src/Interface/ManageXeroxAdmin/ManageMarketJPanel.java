    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = marketlistTable.getSelectedRow();
        
        if(selectedRowValue >=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to Delete this Market", "Warning" , dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
            Market m= (Market)marketlistTable.getValueAt(selectedRowValue, 0);
            ml.removeMarket(m);
            for(MarketOffer mo:moc.getListOfMarketOffer()){
                if(mo.getM().equals(m))
                    moc.removeMarketOffer(mo);
            }
            JOptionPane.showMessageDialog(null,"Record Deleted");
            populateTable();           
           
        }
        }
            else
            JOptionPane.showMessageDialog(null,"Please select a record to delete");    
    
    }//GEN-LAST:event_deletebtnActionPerformed


