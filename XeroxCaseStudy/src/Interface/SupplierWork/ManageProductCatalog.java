    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        int selectedRowValue = productCatalog.getSelectedRow();
        if(selectedRowValue >= 0){
        Product p= (Product)productCatalog.getValueAt(selectedRowValue, 0);
        ViewProduct panel = new ViewProduct(userProcessContainer, p);
        userProcessContainer.add("ViewProduct", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
         else
            JOptionPane.showMessageDialog(null,"Please select a record to view");
    
    }//GEN-LAST:event_btnViewActionPerformed

