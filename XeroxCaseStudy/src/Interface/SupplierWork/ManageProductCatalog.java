   public void populateTable()
   {
       DefaultTableModel dtm = (DefaultTableModel)productCatalog.getModel();
       dtm.setRowCount(0);
       for(Product p:pc.getListofProducts()){
            Object row[] = new Object[4];
            row[0] = p;
            row[1] = p.getProdId();
            row[2] = p.getSupplierPrice();            
            dtm.addRow(row);
       }
        
   }

