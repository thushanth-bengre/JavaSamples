    private void jButtonSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = displayTable.getSelectedRow();
        
        if(selectedRowValue >= 0){
            Product p = (Product) displayTable.getValueAt(selectedRowValue, 0);
            jTextFieldNameOP.setText(p.getName());
            jTextFieldPriceOP.setText(String.valueOf(p.getPrice()));
            jTextFieldQuantityOP.setText(String.valueOf(p.getQty()));
            jTextFieldDescOP.setText(p.getDesc());                   
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record");
    }//GEN-LAST:event_jButtonSelectActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = displayTable.getSelectedRow();
        
        if(selectedRowValue >= 0){
            Product p= (Product)displayTable.getValueAt(selectedRowValue, 0);
            pc.removeProduct(p);
            JOptionPane.showMessageDialog(null,"Record Deleted");
            populateTable();
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record to delete");
    }//GEN-LAST:event_jButtonDeleteActionPerformed


