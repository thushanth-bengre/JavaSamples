    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel)displayTable.getModel();
        dtm.setRowCount(0);
        
        for(Product p : pc.getProductList()){
            Object row[] = new Object[2];
            row[0] = p;
            row[1]= p.getDesc();
            dtm.addRow(row);
        }
    }

