    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        try{String routingNum = jTextFieldRoutingNo.getText();
        String accNo = jTextFieldAccNo.getText();
        String bankName = jTextFieldBankName.getText();
        int balance = Integer.parseInt(jTextFieldBalance.getText());
        if("".equals(routingNum) || "".equals(accNo) || "".equals(bankName) || "".equals(balance))
            JOptionPane.showMessageDialog(null,"Enter All Values","Error", JOptionPane.ERROR_MESSAGE);
        else{
        Account account = ad.addAccount();
        account.setAccountNo(accNo);
        account.setBalance(balance);
        account.setBankName(bankName);
        account.setRoutingNo(routingNum);     
        
        JOptionPane.showMessageDialog(null, "Account Succesfully Created");
        refreshTextBoxes();
        }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Please verify all values entered","Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

