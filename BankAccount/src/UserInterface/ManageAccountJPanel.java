    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = jTableAccntLst.getSelectedRow();
        
        if(selectedRowValue >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to Delete the Account Details", "Warning" , dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
            Account a= (Account)jTableAccntLst.getValueAt(selectedRowValue, 0);
            ad.deleteAccount(a);
            JOptionPane.showMessageDialog(null,"Record Deleted");
            populateTables();
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record to delete");
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = jTableAccntLst.getSelectedRow();
        
        if(selectedRowValue >= 0){
            Account a= (Account)jTableAccntLst.getValueAt(selectedRowValue, 0);
            AccountDetailsJPanel panel = new AccountDetailsJPanel(userProcessContainer,a);
            userProcessContainer.add("AccountDetailsJPanel",panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record to view");
    }//GEN-LAST:event_jButtonViewDetailsActionPerformed

    private void jButtonSearchAccNo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchAccNo1ActionPerformed
        // TODO add your handling code here:
        Account result = ad.searchAccount(jTextFieldAccountNumber.getText());
        if(result == null)
            JOptionPane.showMessageDialog(null,"No result found","Information", JOptionPane.INFORMATION_MESSAGE);
        else{
            AccountDetailsJPanel panel = new AccountDetailsJPanel(userProcessContainer,result);
            userProcessContainer.add("AccountDetailsJPanel",panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            }
            
    }//GEN-LAST:event_jButtonSearchAccNo1ActionPerformed


