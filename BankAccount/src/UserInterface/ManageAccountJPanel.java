    private void populateTables(){
        DefaultTableModel dtm = (DefaultTableModel)jTableAccntLst.getModel();
        dtm.setRowCount(0);
        
        for(Account a : ad.getAccountList()){
            Object row[] = new Object[4];
            row[0] = a;
            row[1]= a.getAccountNo();
            row[2]= a.getBankName();
            row[3]= a.getBalance();
            dtm.addRow(row);
        }
    }

