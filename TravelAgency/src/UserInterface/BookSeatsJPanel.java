    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        String seatNo;
        int age =0;
        try{
            Seats s= f.bookSeats();
            seatNo = s.getSeatNo();
            String firstName = jTextFieldFirstName.getText();
            String lastName = jTextFieldLastName.getText();
            String passportNo = jTextFieldPassportNo.getText();
            age = Integer.parseInt(jTextFieldAge.getText());
            if(firstName == null || passportNo.equals(null) || lastName.equals(null) || age==0)
                JOptionPane.showMessageDialog(null,"Please enter all values","Information", JOptionPane.INFORMATION_MESSAGE);
            else{
                Customer c = cd.addCustomer(f.getFlightId(), firstName, lastName, age, passportNo, seatNo);
                JOptionPane.showMessageDialog(null,"Seat no for this cutomer is "+seatNo ,"Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Please enter numerical values for age","Information", JOptionPane.INFORMATION_MESSAGE);
        }
        
        JOptionPane.showMessageDialog(null,"Customer details added","Information", JOptionPane.INFORMATION_MESSAGE);
        refreshTextBoxes();
    }//GEN-LAST:event_jButtonSaveActionPerformed


