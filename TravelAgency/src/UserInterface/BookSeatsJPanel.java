    private void populateFields(){
        String flightId = f.getFlightId();
        String airlines = null;
        for(Airlines a:ad.getListofAirlines()){
            for(Flights fl: a.getSchedule()){
                if(f.getFlightId().equals(fl.getFlightId()))
                    airlines = a.getName();
            }
        }
        jTextFieldAirliner.setText(airlines);
        jTextFieldFlightId.setText(flightId);
    }
    
