    public void populateTables(){
        DefaultTableModel dtm = (DefaultTableModel)jTableFlightResult.getModel();
        dtm.setRowCount(0);
        for(Flights f: tempList){
            Object row[] = new Object[5];
            row[0] = f;
            row[1]= f.getSrcAirport();
            row[2] = f.getDestAirport();
            row[3] = f.getDeptTime();
            dtm.addRow(row);                  
    }
    }

