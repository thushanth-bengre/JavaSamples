    private void jButtonBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBookActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = jTableFlightResult.getSelectedRow();
        
        if(selectedRowValue >= 0){
            Flights f= (Flights)jTableFlightResult.getValueAt(selectedRowValue, 0);
            BookSeatsJPanel panel = new BookSeatsJPanel(cardSeqJPanel,f,cd,ad);
            cardSeqJPanel.add("BookSeatsJPanel",panel);
            CardLayout layout = (CardLayout) cardSeqJPanel.getLayout();
            layout.next(cardSeqJPanel);
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a flight to book seat"); 
    }//GEN-LAST:event_jButtonBookActionPerformed


