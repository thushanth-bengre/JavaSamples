    public void populateTables(){
        DefaultTableModel dtm = (DefaultTableModel)jTableFlights.getModel();
        dtm.setRowCount(0);
        for(Flights f: a.getSchedule()){
            Object row[] = new Object[5];
            if(null!=f){
            row[0] = f;
            row[1] = f.getSrcAirport();
            row[2] = f.getDestAirport();
            row[3] = f.getDeptTime();            
            dtm.addRow(row);
            }       
    }
    }

