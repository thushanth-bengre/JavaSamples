    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = jTableFlights.getSelectedRow();
        
        if(selectedRowValue >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to Delete the Airliner", "Warning" , dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
            Flights f= (Flights)jTableFlights.getValueAt(selectedRowValue, 0);
            a.removeFight(f);
            cd.deleteFlightCustomers(f.getFlightId());
            JOptionPane.showMessageDialog(null,"Record Deleted");
            populateTables();
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record to delete");
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = jTableFlights.getSelectedRow();
        
        if(selectedRowValue >= 0){
            Flights f= (Flights)jTableFlights.getValueAt(selectedRowValue, 0);
            FlightInfoJPanel panel = new FlightInfoJPanel(cardSeqJPanel,f,cd);
            cardSeqJPanel.add("FlightInfoJPanel",panel);
            CardLayout layout = (CardLayout) cardSeqJPanel.getLayout();
            layout.next(cardSeqJPanel);
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record to view");
        
    }//GEN-LAST:event_jButtonEditActionPerformed

