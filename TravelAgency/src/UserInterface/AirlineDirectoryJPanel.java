    public void populateTables(){
        DefaultTableModel dtm = (DefaultTableModel)jTableAirlineDirectory.getModel();
        dtm.setRowCount(0);
        for(Airlines a: ad.getListofAirlines()){
            Object row[] = new Object[5];
            if(null!=a){
            row[0] = a;
            row[1]= a.getNoOfFlights();
            dtm.addRow(row);
            }       
    }
    }

