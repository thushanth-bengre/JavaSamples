    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = jTableAirlineDirectory.getSelectedRow();
        
        if(selectedRowValue >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to Delete the Airliner", "Warning" , dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
            Airlines a= (Airlines)jTableAirlineDirectory.getValueAt(selectedRowValue, 0);
            ad.removeAirlines(a);
            JOptionPane.showMessageDialog(null,"Record Deleted");
            populateTables();
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record to delete");
        }
    
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonViewActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = jTableAirlineDirectory.getSelectedRow();
        
        if(selectedRowValue >= 0){
            Airlines a= (Airlines)jTableAirlineDirectory.getValueAt(selectedRowValue, 0);
            AirlineInfoJPanel panel = new AirlineInfoJPanel(cardSeqJPanel,a);
            cardSeqJPanel.add("AirlineInfoJPanel",panel);
            CardLayout layout = (CardLayout) cardSeqJPanel.getLayout();
            layout.next(cardSeqJPanel);
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record to view");  
    }//GEN-LAST:event_jButtonViewActionPerformed

    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        Airlines result = ad.searchAirline(jTextFieldSearchAl.getText());
        if(result == null)
            JOptionPane.showMessageDialog(null,"No result found","Information", JOptionPane.INFORMATION_MESSAGE);
        else{
            AirlineInfoJPanel panel = new AirlineInfoJPanel(cardSeqJPanel,result);
            cardSeqJPanel.add("AirlineInfoJPanel",panel);
            CardLayout layout = (CardLayout) cardSeqJPanel.getLayout();
            layout.next(cardSeqJPanel);
            }
    }//GEN-LAST:event_jButtonSearchActionPerformed

