    private void populateTables(){
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        dtm.setRowCount(0);
        for(Customer c : cd.getListOfCustomers()){
            Object row[] = new Object[5];
            if(c!= null){
            row[0] = c;
            row[1]= c.getLastName();
            row[2]= c.getPassportNo();
            row[3]= c.getFlightId();
            row[4]= c.getSeatNo();
            dtm.addRow(row);
            }
            }       
    }

