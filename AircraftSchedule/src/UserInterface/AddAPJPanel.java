    private void jButtonUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUploadActionPerformed
        // TODO add your handling code here:
        Scanner scanner;
        try {
            scanner = new Scanner(new FileReader(filePath));
            scanner.nextLine();
             while(scanner.hasNextLine()){
            String line = scanner.nextLine();
            String[] splits = line.split(",");
            
            String serialNo = splits[0];
            String manuYear = splits[1];
            int noOfSeats = Integer.parseInt(splits[2]);
            String availability = splits[3];
            String manuName =splits[4];
            String modelName = splits[5];
            String srcAirport = splits[6];
            String destAirport = splits[7];
            String deptTime = splits[8];
            String mainExp = splits[9];
            Airplanes a = al.addAirplane();
            Manufacturer m = al.addManufacturer();
            a.setAirliner();
            a.setAvailability(availability);
            a.setMainExpDate(mainExp);
            a.setManuName(manuName);
            a.setManuYear(manuYear);
            a.setModelName(modelName);
            a.setNoOfSeats(noOfSeats);
            a.setSerialNo(serialNo);
            m.setManuName(manuName);
            a.addSchedule(serialNo, srcAirport, destAirport, deptTime);
            al.setCatLstUpd(System.currentTimeMillis());
            
            }
            } catch (FileNotFoundException ex) {
            Logger.getLogger(AddAPJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Records Uploaded");
            jTextField1.setText("");
       
    }//GEN-LAST:event_jButtonUploadActionPerformed


