    private void jButtonConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmActionPerformed
        // TODO add your handling code here:
        
        try{
            String serialNo = jTextFieldSerialNo.getText();
            String manuYear = jTextFieldYearManu.getText();
            int noOfSeats = Integer.parseInt(jTextFieldCapacity.getText());
            String availability = getSelectedButtonText(buttonGroupAvail);
            String manuName = jTextFieldManuName.getText();
            String modelName = jTextFieldModel.getText();
            String srcAirport = jTextFieldSourceAirPort.getText();
            String destAirport = jTextFieldDestAirport.getText();
            String deptTime = jTextFieldDept.getText();
            String mainExp = jTextFieldExpiry.getText();
            Airplanes a = al.addAirplane();
            Manufacturer m = al.addManufacturer();
            a.addSchedule(serialNo, srcAirport, destAirport, deptTime);
            a.setAirliner();
            a.setAvailability(availability);
            a.setMainExpDate(mainExp);
            a.setManuName(manuName);
            a.setManuYear(manuYear);
            a.setModelName(modelName);
            a.setNoOfSeats(noOfSeats);
            a.setSerialNo(serialNo);            
            m.setManuName(manuName);
            al.setCatLstUpd(System.currentTimeMillis());
            JOptionPane.showMessageDialog(null, "Records Added");
            refreshTextBoxes();
            }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please check the values entered");
        }
    }//GEN-LAST:event_jButtonConfirmActionPerformed

