    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel)jTableBoeingPlanes.getModel();
        dtm.setRowCount(0);
        
        for(Airplanes a:tempList ){
            Object row[] = new Object[2];
            row[0] = a;
            row[1] = a.getModelName();
            dtm.addRow(row);
        }
    }
