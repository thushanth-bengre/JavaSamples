    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel)displayTable.getModel();
        dtm.setRowCount(0);
        
        for(Airplanes a : al.getAirplaneList()){
            Object row[] = new Object[2];
            row[0] = a;
            row[1]= a.getManuName();
            dtm.addRow(row);
        }
    }

