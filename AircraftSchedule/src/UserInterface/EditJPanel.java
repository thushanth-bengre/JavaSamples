    private void jButtonViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonViewActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = displayTable.getSelectedRow();
        
        if(selectedRowValue >= 0){
            Airplanes a = (Airplanes) displayTable.getValueAt(selectedRowValue, 0);
            jTextFieldSerialNo.setText(a.getSerialNo());
            jTextFieldManuName.setText(a.getManuName());
            jTextFieldCapacity.setText(String.valueOf(a.getNoOfSeats()));
            jTextFieldModel.setText(a.getModelName());
            jTextFieldYearManu.setText(a.getManuYear());
            Schedule s = a.getSchedule();
            jTextFieldSourceAirport.setText(s.getSrcAirport());
            jTextFieldDestAirport.setText(s.getDestAirpport());
            jTextFieldDept.setText(String.valueOf(s.getDeptTime()));
            String avail = a.isAvailability();
            if(!"Yes".equals(avail))
                jRadioButtonAvailFalse.setSelected(true);            
            else 
                jRadioButtonAvailTrue.setSelected(true);
            
            jTextFieldExpiry.setText(String.valueOf(a.getMainExpDate()));
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record");
    
    }//GEN-LAST:event_jButtonViewActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = displayTable.getSelectedRow();
        
        if(selectedRowValue >= 0){
            Airplanes a= (Airplanes)displayTable.getValueAt(selectedRowValue, 0);
            al.removeAirplanes(a);
            JOptionPane.showMessageDialog(null,"Record Deleted");
            populateTable();
        }
        else
            JOptionPane.showMessageDialog(null,"Please select a record to delete");
    
    }//GEN-LAST:event_jButtonDeleteActionPerformed


