    private void jButtonGoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGoActionPerformed
        // TODO add your handling code here:
        String serialNo = jTextFieldSerialNum.getText();
        boolean foundRec = false;
        for(Airplanes a: al.getAirplaneList()){
            String serialNumber = a.getSerialNo();
            if(serialNo.equals(serialNumber)){
                foundRec = true;
                Schedule s = a.getSchedule();
                jTextFieldAirportOP.setText(s.getSrcAirport());
                jTextFieldAirportOP.setText(s.getSrcAirport());
                jTextFieldCapacityOP.setText(String.valueOf(a.getNoOfSeats()));
                jTextFieldDeptOP.setText(s.getDeptTime());
                jTextFieldDestAirport.setText(s.getDestAirpport());
                jTextFieldExpiryOP.setText(a.getMainExpDate());
                jTextFieldManuNameOP.setText(a.getManuName());
                jTextFieldModelOP.setText(a.getModelName());
                jTextFieldSerialNoOP.setText(a.getSerialNo());
                jTextFieldYearManuOP.setText(a.getManuYear());
            }                
        }
        if(!foundRec)
            JOptionPane.showMessageDialog(null, "No Flights for the given Serial Number. Please run a new Query");
          jTextFieldSerialNum.setText("");
    }//GEN-LAST:event_jButtonGoActionPerformed


