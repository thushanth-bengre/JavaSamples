    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)jTablePersonList.getModel();
        dtm.setRowCount(0);
        for(Person p: pd.getListOfPerson()){
            Object row[] = new Object[3];
            row[0] = p;
            row[1]= p.getLastName();
            row[2] = p.getSsn();
            dtm.addRow(row);                   
    }
    }

