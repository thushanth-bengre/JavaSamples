    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = jTableUserList.getSelectedRow();
        
        if(selectedRowValue >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to Delete this person and associated users", "Warning" , dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION){
                User u= (User)jTableUserList.getValueAt(selectedRowValue, 0);
                ud.removeUser(u.getName());
                JOptionPane.showMessageDialog(null,"Record Deleted");
                populateTable();
        }
            else
            JOptionPane.showMessageDialog(null,"Please select a record to delete");
        }
    
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        String name = jTextFieldUserName.getText();
        if(name == null)
            JOptionPane.showMessageDialog(null,"Enter username!!!");
        else{
            User u = ud.getUserDetails(name);
            EditUserJPanel panel = new EditUserJPanel(cardSeqJPanel,u);
            cardSeqJPanel.add("EditUserJPanel",panel);
            CardLayout layout = (CardLayout) cardSeqJPanel.getLayout();
            layout.next(cardSeqJPanel);
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed

