    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)jTableUserList.getModel();
        dtm.setRowCount(0);
        HashMap<String,User> temphash = ud.getUserMap();
        for(String name: temphash.keySet()){
            User u= temphash.get(name);
            Object row[] = new Object[4];
            row[0] = u;
            row[1]= u.getPassword();
            row[2] = u.getRole();
            row[3] = u.getStatus();
            dtm.addRow(row);                   
    }
    }

